a=int(input("Enter a number :"))
b=int(input("Enter a number:"))

operator=input("Enter a operator:")

if operator =="+":
    print(a+b)
elif operator =="-":
    print(a-b)
elif operator =="*":
    print(a*b)
elif operator =="/":
    print(a/b)
elif operator =="%":
    print(a/b)